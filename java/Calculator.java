/** 
 * The Calculator class provides methods to add and subtract two numbers.
 */
public class Calculator {
    
    /** 
*The add() method returns the sum of two integers.
 
     */
    public static int add(int a, int b) {
        return a + b;
    }

    /** 
*The sub() method returns the difference of two integers.
 
     */
    public static int sub(int a, int b) {
        return a - b;
    }
}
